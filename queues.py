# Queue is the data structure where you can append more data at the end and take out data from the begining. Thats why it is known as FIFO (First In First Out).

a = [1, 2, 3, 4, 5, 6]
a.append(1)
print (a)

a.pop(0)
print(a)

a.pop(0)
print (a)

print (a)

# to take out the first element of the list we are using pop(0).
